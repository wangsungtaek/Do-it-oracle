-- 07-1 하나의 열에 출력 결과를 담는 다중행 함수
-- SUM 함수
SELECT SUM(SAL)
  FROM EMP;
 
SELECT SUM(COMM)
  FROM EMP;
  
SELECT SUM(DISTINCT SAL),
	   SUM(ALL SAL),
	   SUM(SAL)
  FROM EMP;
  
SELECT SUM(SAL), SUM(COMM)
  FROM EMP;
 
-- COUNT 함수
SELECT COUNT(*)
  FROM EMP;
  
SELECT COUNT(*)
  FROM EMP
 WHERE DEPTNO = 30;

SELECT COUNT(DISTINCT SAL),
	   COUNT(ALL SAL),
	   COUNT(SAL)
  FROM EMP;
  
-- NULL값은 제외하고 카운팅 된다. 
SELECT COUNT(COMM)
  FROM EMP;
 
SELECT COUNT(COMM)
  FROM EMP
 WHERE COMM IS NOT NULL;

-- MAX, MIN
SELECT MAX(SAL)
  FROM EMP
 WHERE DEPTNO = 10;

SELECT MIN(SAL)
  FROM EMP
 WHERE DEPTNO = 10;

SELECT MAX(HIREDATE)
  FROM EMP
 WHERE DEPTNO = 20;

SELECT MIN(HIREDATE)
  FROM EMP
 WHERE DEPTNO = 20;

-- AVG 함수
SELECT AVG(SAL)
  FROM EMP
 WHERE DEPTNO = 30;

SELECT AVG(DISTINCT SAL)
  FROM EMP
 WHERE DEPTNO = 30;

SELECT AVG(COMM)
  FROM EMP
 WHERE DEPTNO = 30;
